package backingBean;

public class Dept {
    private int id;
    private String username;
    private String hod;

    public Dept() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String email) {
        this.username = email;
    }

    public String getHod() {
        return hod;
    }

    public void setHod(String password) {
        this.hod = password;
    }


}
